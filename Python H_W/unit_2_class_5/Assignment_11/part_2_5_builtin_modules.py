import math

num = float(input("Enter a number: "))              # Output: Enter a number: 69
print(f"Square root: {math.sqrt(num)}")             # Output: Square root: 8.306623862918075
print(f"Factorial: {math.factorial(int(num))}")     # Output: Factorial: 171122452428141311372468338881272839092270544893520369393648040923257279754140647424000000000000000
print(f"Logarithm: {math.log(num)}")                # Output: Logarithm: 4.23410650459726