#simple assignment
r = 420
print(r) #output: 420

# addition assignment
r += 120 # g = g + 40
print(r) #output: 540

#subtraction assignment
r -= 52 # g = g - 35
print(r) #output: 488

#multiplication assignment
r *= 85 # g = g * 48
print(r) #output: 41480

#Division assignment
r /= 99 # r / r * 99
print(r) #output: 418.989898989899
