num = int(input("Enter a number: ")) # Output: Enter a number: 40

if num > 10:
    print("The number is greater than 10.") # Output: The number is greater than 10.
if num < 50:
    print("The number is less than 50.") # Output: The number is less than 50.
if num % 2 == 0 and num % 5 == 0:
    print("The number is divisible by both 2 and 5.") # Output: The number is divisible by both 2 and 5.
else:
    print("The number is not divisible by both 2 and 5.") # Output: The number is not divisible by both 2 and 5.

